# ask the user for two numbers
# output the sum, difference, product & quotient of numbers

#

num1 = float(input("Enter the first number: "))
num2 = float(input("Enter the second number: "))

print("The sum of", num1, "and", num2, "is",
      num1 + num2)
print("The difference of", num1, "and", num2, "is",
      num1 - num2)
print("The product of", num1, "and", num2, "is",
      num1 * num2)
print("The quotient of", num1, "and", num2, "is",
      num1 / num2)


